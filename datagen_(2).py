    ax.bar(x, -vq_summary["ncl_r"], label="NCL Rate", color="#d62728", alpha=0.75)
    ax.bar(x, -vq_summary["cof_r"], bottom=-vq_summary["ncl_r"],
           label="Cost of Funds", color="#ff7f0e", alpha=0.75)
    ax.plot(x, vq_summary["pnl_r"], color="black", lw=2, marker="o",
            markersize=3, label="Net P&L Rate")
    ax.axhline(0, color="grey", lw=0.8)
    ax.yaxis.set_major_formatter(mtick.PercentFormatter())
    ax.set_xticks(x[::4]); ax.set_xticklabels(vq_summary["vintage_quarter"].iloc[::4],
                                                rotation=35, fontsize=8)
    ax.set_title("P&L Components by Origination Quarter (% ANR ann.)")
    ax.set_ylabel("Rate (% ANR ann.)"); ax.legend(fontsize=8)

    # ── D: Break-even MOB by vintage year ────────────────────────────────────
    ax = axes[1,1]
    be_mob = {}
    for yr in years:
        sub = vy_pnl[vy_pnl["vintage_year"]==yr].sort_values("months_on_book")
        cum_pnl = sub["pnl"].cumsum()
        be_idx  = cum_pnl[cum_pnl>=0].index
        be_mob[yr] = sub.loc[be_idx[0],"months_on_book"] if len(be_idx)>0 else np.nan
    be_df = pd.DataFrame(list(be_mob.items()), columns=["vintage_year","break_even_mob"])
    bars  = ax.bar(be_df["vintage_year"].astype(str), be_df["break_even_mob"],
                   color=[plt.cm.tab10(i/10) for i in range(len(be_df))], alpha=0.85)
    for bar, v in zip(bars, be_df["break_even_mob"]):
        if not np.isnan(v):
            ax.text(bar.get_x()+bar.get_width()/2, v+0.3, f"{v:.0f}m",
                    ha="center", va="bottom", fontsize=9)
