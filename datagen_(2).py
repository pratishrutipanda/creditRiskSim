    ax.tick_params(axis="y", labelsize=6)

    # ── C: Revenue, NCL, CoF, P&L breakdown by origination quarter ───────────
    ax = axes[1,0]
    vq_summary = panel.groupby("vintage_quarter").agg(
        rev=("cnr","sum"), ncl=("ncl","sum"),
        cof=("cost_of_funds","sum"), pnl=("pnl","sum"),
        anr=("ANR","sum")).reset_index()
    vq_summary["rev_r"] = vq_summary["rev"] / vq_summary["anr"].replace(0,np.nan) * 12 * 100
