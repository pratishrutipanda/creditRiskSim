def plot_scenario_sensitivity(panel: pd.DataFrame):
    """Fig 09 — P&L sensitivity to NCL rate shocks and revenue shocks."""
    fig, axes = plt.subplots(1, 2, figsize=(16, 7))
    fig.suptitle("P&L Scenario Sensitivity Analysis", fontsize=15, fontweight="bold")

    # Base metrics (annualised, $M)
    base_anr     = panel["ANR"].sum() / 5        # 5-year portfolio
    base_rev     = panel["cnr"].sum() / 5
    base_ncl     = panel["ncl"].sum() / 5
    base_cof     = panel["cost_of_funds"].sum() / 5
    base_opex    = panel["opex"].sum() / 5
    base_pnl     = base_rev - base_ncl - base_cof - base_opex

    ncl_rate_base = base_ncl / base_anr

    # ── A: NCL stress tornado ─────────────────────────────────────────────────
    ax = axes[0]
    shocks   = [-0.020, -0.010, 0.000, +0.010, +0.020, +0.030, +0.050]  # absolute pp on NCL rate
    pnl_vals = [(base_rev - (ncl_rate_base + s)*base_anr - base_cof - base_opex)/1e6
                for s in shocks]
    shock_labels = [f"{'+' if s>=0 else ''}{s*100:.0f}pp" for s in shocks]
    bar_colors   = ["#2ca02c" if v>=base_pnl/1e6 else "#d62728" for v in pnl_vals]
    ax.barh(shock_labels, pnl_vals, color=bar_colors, alpha=0.85, edgecolor="white")
    ax.axvline(base_pnl/1e6, color="black", lw=1.5, ls="--", label=f"Base P&L ${base_pnl/1e6:.0f}M")
    ax.xaxis.set_major_formatter(mtick.FuncFormatter(lambda x,_:f"${x:.0f}M"))
    ax.set_title("Net P&L Sensitivity to NCL Rate Shocks (±pp)\n(Annual, $M)")
    ax.set_xlabel("Net P&L ($M, ann.)"); ax.legend(fontsize=9)
    for i, v in enumerate(pnl_vals):
        ax.text(v + (base_pnl/1e6)*0.01, i, f"${v:.0f}M", va="center", fontsize=8)

    # ── B: 2-way sensitivity table (NCL shock × revenue shock) ────────────────
    ax = axes[1]
    ncl_shocks = [-0.010, 0.0, +0.010, +0.020, +0.030]
    rev_shocks = [-0.05, -0.02, 0.0, +0.02, +0.05]
    table_data = np.zeros((len(ncl_shocks), len(rev_shocks)))
    for i, ns in enumerate(ncl_shocks):
        for j, rs in enumerate(rev_shocks):
            pnl_s = (base_rev*(1+rs) - (ncl_rate_base+ns)*base_anr - base_cof - base_opex)/1e6
            table_data[i,j] = pnl_s

    sns.heatmap(table_data,
                xticklabels=[f"{'+' if r>=0 else ''}{r*100:.0f}%" for r in rev_shocks],
                yticklabels=[f"{'+' if n>=0 else ''}{n*100:.0f}pp" for n in ncl_shocks],
                annot=True, fmt=".0f", cmap="RdYlGn", center=base_pnl/1e6,
                ax=ax, cbar_kws={"label":"Net P&L $M (ann.)"}, linewidths=0.3)
    ax.set_xlabel("Revenue Rate Shock"); ax.set_ylabel("NCL Rate Shock")
    ax.set_title("2-Way P&L Sensitivity: NCL Shock × Revenue Shock\n($M ann. — green = above base)")

    plt.tight_layout()
    path = f"{OUTPUT_DIR}/09_scenario_sensitivity.png"
    plt.savefig(path, dpi=150, bbox_inches="tight")
    plt.close()
    print(f"  ✔ Saved {path}")

