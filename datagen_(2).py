    x = np.arange(len(q_pnl))
    ax.bar(x, q_pnl["rev_rate"], label="Revenue Rate", color="#2f4b7c", alpha=0.7)
    ax.bar(x, -q_pnl["ncl_rate"], label="NCL Rate", color="#d62728", alpha=0.7)
