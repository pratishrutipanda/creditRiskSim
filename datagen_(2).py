def plot_pnl_by_vintage(panel: pd.DataFrame):
    """Fig 07 — P&L breakdown by origination month / quarter."""
    fig, axes = plt.subplots(2, 2, figsize=(16, 12))
    fig.suptitle("P&L Analysis by Origination Vintage", fontsize=15, fontweight="bold")

    # ── A: Cumulative P&L by vintage year ────────────────────────────────────
    ax = axes[0,0]
    vy_pnl = panel.groupby(["vintage_year","months_on_book"]).agg(
        pnl=("pnl","sum"), anr=("ANR","sum")).reset_index()
    vy_pnl = vy_pnl.sort_values(["vintage_year","months_on_book"])
    years = sorted(vy_pnl["vintage_year"].unique())
    cmap  = plt.cm.tab10(np.linspace(0,0.9,len(years)))
    for yr, color in zip(years, cmap):
        sub = vy_pnl[vy_pnl["vintage_year"]==yr]
        cum = sub["pnl"].cumsum() / 1e6
        ax.plot(sub["months_on_book"], cum, label=str(yr), color=color, lw=2)
    ax.axhline(0, color="grey", lw=0.8, ls="--")
    ax.set_title("Cumulative P&L by Vintage Year")
    ax.set_xlabel("Months on Book"); ax.set_ylabel("Cumulative P&L ($M)")
    ax.yaxis.set_major_formatter(mtick.FuncFormatter(lambda x,_:f"${x:.0f}M"))
    ax.legend(title="Vintage Year", fontsize=8)

    # ── B: P&L heatmap (vintage quarter × observation year) ──────────────────
    ax = axes[0,1]
    vq_yr = panel.groupby(["vintage_quarter","year"]).agg(
        pnl=("pnl","sum"), anr=("ANR","sum")).reset_index()
    vq_yr["pnl_rate"] = vq_yr["pnl"] / vq_yr["anr"].replace(0,np.nan) * 12 * 100
    pivot = vq_yr.pivot(index="vintage_quarter", columns="year", values="pnl_rate")
    sns.heatmap(pivot, annot=True, fmt=".1f", cmap="RdYlGn",
                center=0, linewidths=0.3, ax=ax, cbar_kws={"label":"Net P&L Rate % ann."})
