CHUNK = 500

for i in range(N_ACCOUNTS):
    if i % CHUNK == 0:
        print(f"  [{i:>6,}/{N_ACCOUNTS:,}] accounts processed...", flush=True)
    row = df.iloc[i]
    acct_id    = row[ "account_id"]
    orig       = row[ "orig_date"]
    c_limit    = row[ "credit_limit"]
    fico_i     = row[ "fico_score"]
    income_i   = row[ "income_k"]
    pfull_i    = row[ "prob_pay_full"]
    bsf_i      = row[ "base_spend_frac"]
    apr_i      = row[ "apr"] / 100 / 12   # monthly interest rate
    util_i     = row[ "utilization_rate_behavioural"]
    monthlySpend_i = row[ "monthly_spend_behavioural"]
    openingBalance_i = row[ "closingBalance"]
    dpd_i = row[ "dpd_bucket"]
    workingInterest = (row["closingBalance"] - row["paymentMade"])*apr_i
    workingPayment = row["paymentMade"]
    workingLateFess = 0
    availableLimit = row["limitAvailable"]

    bucket1BreachCount = 0
    if dpd_i >= 2:
        bucket1BreachCount = 1
    else:
        bucket1BreachCount = 0

    if dpd_i ==6:
        is_co   = True
    else:
        is_co = False
        min_due_paid = 1

    for m in ALL_MONTHS:
        if m < orig:
            continue
        min_due_paid = 1
        mob = (m.year - orig.year) * 12 + (m.month - orig.month)  # months on book
        gcl_ind = 0
        # ── Balance dynamics ──────────────────────────────────────────────────
        if not is_co:
            MAX_UTIL_THRESHOLD = 0.9 * np.random.normal(1, 0.002)

# --- OCL (unchanged) ---
            ocl_approved = (mob >= 12) and (fico_i >= 780) and (dpd_i == 0)
            if dpd_i <= 0:
                spend_cap = c_limit * 1.20     if ocl_approved else c_limit
            elif dpd_i <= 2:
                spend_cap = c_limit
            else:
                spend_cap = 0.0

            avail_limit = max(spend_cap - openingBalance_i + workingPayment, 0)
            util_vs_climit = openingBalance_i / c_limit
            excess_util = max(util_vs_climit - 0.80, 0)
            # spend_dampener = np.exp(-3.5 * excess_util)
# --- MOB ramp: new accounts spend conservatively ---
            mob_ramp = min(5.0, mob)  # 0.6 at MOB=1, 1.0 at MOB=5+

# --- Season: Q4 higher, Q1 lower (peak December, trough February) ---
            season = 2.0 + np.sin((m.month - 3) * np.pi / 6)

# --- Base spend: income proxy is the primary cap ---
# util_i drives how much of monthlySpend they actually put on the card
            base_spend = monthlySpend_i * util_i * mob_ramp
# --- Raw spend: dampener only meaningful near limit ---
            raw_spend = min(base_spend, avail_limit) * season * np.random.normal(10, 15)

            spend = np.clip(raw_spend, 0, avail_limit*0.75 if avail_limit > 0 else 0)

            # New balance before fees and payment
            new_balance_a = openingBalance_i + spend + workingInterest +workingLateFess

            if openingBalance_i - workingPayment + spend < c_limit:
                interest = np.clip((openingBalance_i - workingPayment + spend) * apr_i, 0 , openingBalance_i - workingPayment + spend)
            else:
                interest = np.clip((openingBalance_i - workingPayment + spend) * (apr_i+0.005), 0 , openingBalance_i - workingPayment + spend)

            # ── Late fee: $30, applied to 5% of delinquent billing records ──
            ocl_fee = 0.0
            # if new_balance_a > c_limit:
            #     raw_ocl_fee = 30.0
            #     over_limit = new_balance_a - c_limit
            #     # Fee cannot exceed amount over limit; cap to over_limit - 1
            #     ocl_fee = round(min(over_limit, raw_ocl_fee), 2)

            # Add fees to balance
            new_balance = new_balance_a + ocl_fee
            closing_balance = max(new_balance - workingPayment , 0.0)

            if dpd_i == 0:
                minDue = np.clip((openingBalance_i + spend)* 0.02 + workingInterest + workingLateFess + ocl_fee, 0, closing_balance)
            elif dpd_i == 1 or dpd_i == 2:
                minDue = np.clip((openingBalance_i + spend)* 0.05 + workingInterest + workingLateFess + ocl_fee, 0, closing_balance)
