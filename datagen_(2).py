def behavioural_risk_segmentation(panel: pd.DataFrame, output_dir: str = OUTPUT_DIR):
    """
    Behavioural risk segmentation framework.

    Builds account-level behavioural features from panel history,
    scores and tiers each account using rule-based logic, and
    produces NCL/PnL impact summary + plots.

    Does NOT modify the original panel — all outputs are returned
    as separate dataframes.

    Returns
    -------
    behav       : account-level feature matrix with risk scores and tiers
    tier_summary: aggregated NCL/PnL metrics by behavioural risk tier
    """
    import matplotlib.dates as mdates

    TIER_ORDER  = ["Very Low Risk","Low Risk","Medium Risk","High Risk","Very High Risk"]
    TIER_COLORS = {
        "Very Low Risk":  "#2ca02c",
        "Low Risk":       "#98df8a",
        "Medium Risk":    "#ffcc00",
        "High Risk":      "#ff7f0e",
        "Very High Risk": "#d62728",
    }

    # ── PART A: Account-level behavioural feature matrix ─────────────────────
    # Aggregate from panel history — excludes post charge-off months
    print("  Building behavioural feature matrix...")

    behav = (
        panel[panel["is_charged_off"] == 0]
        .groupby("account_id")
        .agg(
            # Spend
            avg_monthly_spend     = ("monthly_spend_this_month", "mean"),
            spend_volatility      = ("monthly_spend_this_month", "std"),
            # Utilization
            avg_utilization       = ("utilization_rate",         "mean"),
            max_utilization       = ("utilization_rate",         "max"),
            # Payment
            avg_payment_ratio     = ("payment_ratio",            "mean"),
            min_payment_ratio     = ("payment_ratio",            "min"),
            # Delinquency
            max_dpd_reached       = ("dpd_bucket_num",           "max"),
            months_delinquent     = ("dpd_bucket_num",           lambda x: (x > 0).sum()),
            ever_90dpd            = ("dpd_bucket_num",           lambda x: int((x >= 3).any())),
            # Fee behaviour
            total_late_fees       = ("late_fee",                 "sum"),
            total_ocl_fees        = ("ocl_fee",                  "sum"),
            months_with_late_fee  = ("late_fee",                 lambda x: (x > 0).sum()),
            # P&L
            total_ncl             = ("ncl",                      "sum"),
            total_pnl             = ("pnl",                      "sum"),
            total_cnr             = ("cnr",                      "sum"),
            avg_anr               = ("ANR",                      "mean"),
            total_anr             = ("ANR",                      "sum"),
            # Metadata
            months_observed       = ("month",                    "count"),
            fico_score            = ("fico_score",               "first"),
            income_k              = ("income_k",                 "first"),
        )
        .reset_index()
    )

    # Derived ratios
    behav["delinquency_rate"]   = (
        behav["months_delinquent"] / behav["months_observed"]
    )
    behav["late_fee_frequency"] = (
        behav["months_with_late_fee"] / behav["months_observed"]
    )
    behav["ncl_rate_ann"]       = (
        behav["total_ncl"] /
        (behav["avg_anr"] * behav["months_observed"] / 12)
    ).fillna(0)

    # ── PART B: Rule-based risk scoring ──────────────────────────────────────
    print("  Scoring accounts...")

    def score_account(row):
        score = 0

        # Utilization (0–30 pts)
        if   row["avg_utilization"] >= 0.90: score += 30
        elif row["avg_utilization"] >= 0.75: score += 20
        elif row["avg_utilization"] >= 0.50: score += 10

        # Payment ratio (0–30 pts)
        if   row["avg_payment_ratio"] < 0.10: score += 30
        elif row["avg_payment_ratio"] < 0.20: score += 20
        elif row["avg_payment_ratio"] < 0.40: score += 10

        # Delinquency history (0–25 pts)
        if   row["max_dpd_reached"] >= 3: score += 25
        elif row["max_dpd_reached"] >= 1: score += 15

        if   row["delinquency_rate"] >= 0.20: score += 10
        elif row["delinquency_rate"] >= 0.05: score +=  5

        # Fee behaviour (0–15 pts)
        if   row["late_fee_frequency"] >= 0.20: score += 15
        elif row["late_fee_frequency"] >= 0.05: score +=  8

        return score

    behav["risk_score"] = behav.apply(score_account, axis=1)

    behav["behav_risk_tier"] = pd.Categorical(
        behav["risk_score"].map(lambda s:
            "Very High Risk" if s >= 80 else
            "High Risk"      if s >= 60 else
            "Medium Risk"    if s >= 40 else
            "Low Risk"       if s >= 20 else
            "Very Low Risk"
        ),
        categories=TIER_ORDER,
        ordered=True
    )

    print("  Tier distribution:")
    print(behav["behav_risk_tier"].value_counts().sort_index().to_string())

    # ── PART C: NCL/PnL summary by tier ──────────────────────────────────────
    # Join tier back to panel temporarily (read-only — panel not modified)
    print("  Building tier summary...")

    panel_with_tier = panel.merge(
        behav[["account_id","behav_risk_tier"]],
        on="account_id", how="left"
    )

    tier_summary = (
        panel_with_tier.groupby("behav_risk_tier", observed=True)
        .agg(
            accounts          = ("account_id",        "nunique"),
            total_anr         = ("ANR",               "sum"),
            avg_anr           = ("ANR",               "mean"),
            total_ncl         = ("ncl",               "sum"),
            total_cnr         = ("cnr",               "sum"),
            total_pnl         = ("pnl",               "sum"),
            avg_utilization   = ("utilization_rate",  "mean"),
            avg_payment_ratio = ("payment_ratio",     "mean"),
        )
        .reset_index()
    )

    tier_summary["ncl_rate_ann"]  = (
        tier_summary["total_ncl"] / tier_summary["total_anr"] * 12 * 100
    )
    tier_summary["pnl_per_acct"]  = tier_summary["total_pnl"] / tier_summary["accounts"]
    tier_summary["ncl_per_acct"]  = tier_summary["total_ncl"] / tier_summary["accounts"]
    tier_summary["cnr_margin"]    = (
        tier_summary["total_cnr"] / tier_summary["total_anr"] * 100
    )

    display(tier_summary.style
        .format({
            "total_anr":         "${:,.0f}",
            "avg_anr":           "${:,.0f}",
            "total_ncl":         "${:,.0f}",
            "total_cnr":         "${:,.0f}",
            "total_pnl":         "${:,.0f}",
            "ncl_rate_ann":      "{:.2f}%",
            "pnl_per_acct":      "${:,.0f}",
            "ncl_per_acct":      "${:,.0f}",
            "cnr_margin":        "{:.2f}%",
            "avg_utilization":   "{:.1%}",
            "avg_payment_ratio": "{:.1%}",
        })
        .background_gradient(subset=["ncl_rate_ann"], cmap="RdYlGn_r")
        .background_gradient(subset=["total_pnl"],    cmap="RdYlGn")
        .set_caption("NCL Impact and P&L by Behavioural Risk Tier")
    )

    # ── PART D: Plots ─────────────────────────────────────────────────────────
    print("  Plotting...")

    colors = [TIER_COLORS[t] for t in TIER_ORDER]
    ts     = tier_summary.set_index("behav_risk_tier").reindex(TIER_ORDER)

    fig, axes = plt.subplots(2, 3, figsize=(22, 12))
    fig.suptitle("Behavioural Risk Segmentation — NCL & P&L Impact",
                 fontsize=15, fontweight="bold")

    # Panel 1: Account count
    ax = axes[0, 0]
    ax.bar(TIER_ORDER, ts["accounts"], color=colors, alpha=0.85, edgecolor="white")
    ax.set_title("Account Count by Risk Tier")
    ax.set_ylabel("Accounts")
    ax.set_xticklabels(TIER_ORDER, rotation=20, ha="right")
    ax.grid(True, alpha=0.3, axis="y")

    # Panel 2: Annualised NCL rate
    ax = axes[0, 1]
    ax.bar(TIER_ORDER, ts["ncl_rate_ann"], color=colors, alpha=0.85, edgecolor="white")
    ax.yaxis.set_major_formatter(mtick.PercentFormatter())
    ax.set_title("Annualised NCL Rate by Risk Tier")
    ax.set_ylabel("NCL Rate % of ANR")
    ax.set_xticklabels(TIER_ORDER, rotation=20, ha="right")
    ax.grid(True, alpha=0.3, axis="y")

    # Panel 3: Total P&L
    ax = axes[0, 2]
    pnl_vals   = ts["total_pnl"] / 1e6
    bar_colors = ["#d62728" if v < 0 else c for v, c in zip(pnl_vals, colors)]
    ax.bar(TIER_ORDER, pnl_vals, color=bar_colors, alpha=0.85, edgecolor="white")
    ax.yaxis.set_major_formatter(mtick.FuncFormatter(lambda x, _: f"${x:.1f}M"))
    ax.axhline(0, color="black", lw=0.8, linestyle="--")
    ax.set_title("Total P&L by Risk Tier")
    ax.set_ylabel("P&L ($M)")
    ax.set_xticklabels(TIER_ORDER, rotation=20, ha="right")
    ax.grid(True, alpha=0.3, axis="y")

    # Panel 4: Utilization vs payment ratio scatter
    ax = axes[1, 0]
    for tier, color in TIER_COLORS.items():
        sub = behav[behav["behav_risk_tier"] == tier]
        ax.scatter(
            sub["avg_utilization"], sub["avg_payment_ratio"],
            label=tier, color=color, alpha=0.4, s=10
        )
    ax.set_title("Utilization vs Payment Ratio\n(coloured by risk tier)")
    ax.set_xlabel("Avg Utilization Rate")
    ax.set_ylabel("Avg Payment Ratio")
    ax.xaxis.set_major_formatter(mtick.PercentFormatter(1))
    ax.yaxis.set_major_formatter(mtick.PercentFormatter(1))
    ax.legend(fontsize=7, markerscale=2)
    ax.grid(True, alpha=0.3)

    # Panel 5: NCL per account
    ax = axes[1, 1]
    ax.bar(TIER_ORDER, ts["ncl_per_acct"], color=colors, alpha=0.85, edgecolor="white")
    ax.yaxis.set_major_formatter(mtick.FuncFormatter(lambda x, _: f"${x:,.0f}"))
    ax.set_title("NCL per Account by Risk Tier")
    ax.set_ylabel("NCL per Account ($)")
    ax.set_xticklabels(TIER_ORDER, rotation=20, ha="right")
    ax.grid(True, alpha=0.3, axis="y")

    # Panel 6: CNR margin
    ax = axes[1, 2]
    ax.bar(TIER_ORDER, ts["cnr_margin"], color=colors, alpha=0.85, edgecolor="white")
    ax.yaxis.set_major_formatter(mtick.PercentFormatter())
    ax.axhline(0, color="black", lw=0.8, linestyle="--")
    ax.set_title("CNR Margin % of ANR by Risk Tier")
    ax.set_ylabel("CNR Margin %")
    ax.set_xticklabels(TIER_ORDER, rotation=20, ha="right")
    ax.grid(True, alpha=0.3, axis="y")

    plt.tight_layout()
    path = f"{output_dir}/behavioural_risk_segmentation.png"
    plt.savefig(path, dpi=150, bbox_inches="tight")
    plt.close()
    print(f"  ✔ Saved {path}")

    # ── Summary print ─────────────────────────────────────────────────────────
    print(f"\n  Portfolio NCL breakdown by behavioural tier:")
    for _, row in tier_summary.iterrows():
        print(f"    {row['behav_risk_tier']:<16} | "
              f"{row['accounts']:>5,} accts | "
              f"NCL rate: {row['ncl_rate_ann']:>5.2f}% | "
              f"P&L: ${row['total_pnl']/1e6:>6.2f}M")

    return behav, tier_summary
