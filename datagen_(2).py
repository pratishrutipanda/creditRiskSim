def plot_pnl_waterfall(panel: pd.DataFrame):
    """Fig 06 — Full-portfolio annual P&L waterfall + quarterly trend + YoY P&L."""
    fig, axes = plt.subplots(1, 3, figsize=(24, 8))
    fig.suptitle("Portfolio P&L Analysis", fontsize=15, fontweight="bold")

    # ── Annual waterfall (all years combined) ─────────────────────────────────
    ax = axes[0]
    totals = {
        "Avg Net\nReceivables":  monthly["anr"].sum()/60,
        "Interest\nIncome":      monthly["interest_income"].sum()/5,
        "Interchange\nIncome":   monthly["interchange_income"].sum()/5,
        "Fee\nIncome":           (monthly["late_fee"].sum() + panel["ocl_fee"].sum())/5,
        "Total\nRevenue":        monthly["cnr"].sum()/5,
        "Net Credit\nLoss":      -monthly["ncl"].sum()/5,
        "Cost of\nFunds":        -monthly["cost_of_funds"].sum()/5,
        "OpEx":                  -monthly["opex"].sum()/5,
        "Net P&L":               monthly["pnl"].sum()/5,
    }
    labels  = list(totals.keys())
    values  = [v/1e6 for v in totals.values()]
    running = 0.0
    bottoms = []
    bar_colors = []
    for lab, val in zip(labels, values):
        if lab in ("Avg Net\nReceivables","Total\nRevenue","Net P&L"):
            bottoms.append(0); bar_colors.append("#2f4b7c")
            running = val
        elif val >= 0:
            bottoms.append(running); running += val; bar_colors.append("#2ca02c")
        else:
            running += val; bottoms.append(running); bar_colors.append("#d62728")
    bars = ax.bar(labels, values, bottom=bottoms, color=bar_colors, alpha=0.85,
                  edgecolor="white", width=0.6)
    for bar, v, b in zip(bars, values, bottoms):
        ypos = b + v + (abs(v)*0.02)
